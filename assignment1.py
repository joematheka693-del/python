# 2. Find the Area and perimeter of a rectangle whose length is 48cm and width is 25cm.

# Area
length = 48
width = 25
Area= length * width
print("The area of the rectangle is: ", Area)

# Perimeter
Perimeter = (length + width) * 2
print("The perimeter of the rectangle is: ", Perimeter)


